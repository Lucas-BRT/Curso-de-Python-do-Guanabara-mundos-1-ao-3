i = input('digite algo: ')

print('você digitou um dado do tipo {}'.format(type(i)))
print('{} é um espaço? {}'.format(i, i.isspace()) )
print('{} é alfanumerico? {}'.format(i, i.isalnum()))
print('{} é alfabetico? {}'.format(i, i.isalpha()))
print('{} é numerico? {}'.format(i, i.isnumeric()))
print('{} é um número decimal? {}'.format(i, i.isdecimal()))
print('{} é minúsculo? {}'.format(i, i.islower()))
print('{} é printável? {}'.format(i, i.isprintable()))





